//使用するヘッダーファイル
#include "GameL\DrawTexture.h"
#include "GameHead.h"
#include "Diffusion.h"
#include "GameL\HitBoxManager.h"
#include "GameL\Audio.h"

//使用するネームスペース
using namespace GameL;

//コンストラクタ
CObjDifBullet::CObjDifBullet(float x, float y, float r, float spe)
{
	m_x = x;
	m_y = y;
	m_r = r-1000;
	m_speed = spe;
}

//イニシャライズ
void CObjDifBullet::Init()
{
	m_vx = cos(3.14f/180.0f*m_r);
	m_vy = sin(3.14f / 180.0f * m_r);
	
	//当たり判定作成
	Hits::SetHitBox(this, m_x, m_y, 32, 32, ELEMENT_BULLET, OBJ_DIFFUSION, 1);
}

//アクション
void CObjDifBullet::Action()
{
	m_x += m_vx * m_speed;
	m_y -= m_vy * m_speed;

	//hitbox更新用ポインターの取得
	CHitBox* hit = Hits::GetHitBox(this);
	hit->SetPos(m_x, m_y);
	
	//領域外に出たら削除
	if (m_x < -32.0f)
	{
		this->SetStatus(false);
		Hits::DeleteHitBox(this);
	}
	if (m_y < -32.0f)
	{
		this->SetStatus(false);
		Hits::DeleteHitBox(this);
	}
	if (m_x > 800.0f)
	{
		this->SetStatus(false);
		Hits::DeleteHitBox(this);
	}
	if (m_y > 600.0f)
	{
		this->SetStatus(false);
		Hits::DeleteHitBox(this);
	}







	//ELEMENT_ENEMYを持つオブジェクトと接触したら削除
	if (hit->CheckElementHit(ELEMENT_ENEMY) == true)
	{
		Audio::Start(3);//着弾音を鳴らす

		this->SetStatus(false);
		Hits::DeleteHitBox(this);
	}
}

//ドロー
void CObjDifBullet::Draw()
{
	//描写カラー情報 R=RED G=Green B=Blue A=alpha
	float c[4] = { 1.0f,1.0f,1.0f,1.0f };

	RECT_F src;
	RECT_F dst;

	//切り取り位置の設定
	src.m_top = 0.0f;
	src.m_left = 0.0f;
	src.m_right = 50.0f;
	src.m_bottom = 50.0f;

	//表示位置の設定
	dst.m_top = 0.0f + m_y;
	dst.m_left = 0.0f + m_x;
	dst.m_right = 32.0f + m_x;
	dst.m_bottom = 32.0f + m_y;

	//1番に登録したグラフィックをの情報をもとに描画
	Draw::Draw(1, &src, &dst, c, m_r);
}